package ch.mpetermann.fhnw.oop1.ctrl_struct;

public class Counter {

  public static void main(String[] args) {
    System.out.println("For loop");
    // for loop
    for (int i = 1; i <= 10; i++) {
        System.out.println(i);
    }

    System.out.println("While loop");
    // while loop
    int i = 1;
    while (i <=10) {
      System.out.println(i);
      i++;
    }

  }
}
